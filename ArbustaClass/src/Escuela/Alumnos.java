package Escuela;

public class Alumnos {
private String nombre;
private String apellido;
private int legajo;
private int notademat;
private int notadeleng;
private int notadeprog;
private int sumanotas;
private float resultado;

public String getNombre() {
	return nombre;
}
public void setNombre(String nombre) {
	this.nombre = nombre;
}
public String getApellido() {
	return apellido;
}
public void setApellido(String apellido) {
	this.apellido = apellido;
}
public int getLegajo() {
	return legajo;
}
public void setLegajo(int legajo) {
	this.legajo = legajo;
}
public int getNotademat() {
	return notademat;
}
public void setNotademat(int notademat) {
	this.notademat = notademat;
}
public int getNotadeleng() {
	return notadeleng;
}
public void setNotadeleng(int notadelen) {
	this.notadeleng = notadelen;
}
public int getNotadeprog() {
	return notadeprog;
}
public void setNotadeprog(int notadeprog) {
	this.notadeprog = notadeprog;
}
public int getSumanotas() {
	return sumanotas;
}
public void setSumanotas(int sumanotas) {
	this.sumanotas = sumanotas;
}
public float getResultado() {
	return resultado;
}
public void setResultado(float resultado) {
	this.resultado = resultado;
}





}
